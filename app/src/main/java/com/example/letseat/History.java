package com.example.letseat;

public class History {
    private String tanggal;
    private String asamurat;
    private String kolestrol;
    private String guladarah;

    public History() {
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getAsamurat() {
        return asamurat;
    }

    public void setAsamurat(String asamurat) {
        this.asamurat = asamurat;
    }

    public String getKolestrol() {
        return kolestrol;
    }

    public void setKolestrol(String kolestrol) {
        this.kolestrol = kolestrol;
    }

    public String getGuladarah() {
        return guladarah;
    }

    public void setGuladarah(String guladarah) {
        this.guladarah = guladarah;
    }
}
